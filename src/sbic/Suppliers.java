    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed

        try {
            if (!selectedSupplier.canDelete()) {

                JOptionPane.showMessageDialog(this, "You can not delete this Supplier because it has items attached to it.", "Delete Supplier", JOptionPane.ERROR_MESSAGE);
            } else {

                if (JOptionPane.showConfirmDialog(this, "Are You Sure You Want To Delete " + selectedSupplier.getName() + "?", "Delete Supplier", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE) == JOptionPane.YES_OPTION) {

                    if (selectedSupplier.delete()) {
                        JOptionPane.showMessageDialog(this, selectedSupplier.getName() + " deleted succesfully!", "Delete Supplier", JOptionPane.INFORMATION_MESSAGE);
                        loadSuppliers();
                    } else {
                        JOptionPane.showMessageDialog(this, "Deleting " + selectedSupplier.getName() + " failed!", "Delete Supplier", JOptionPane.ERROR_MESSAGE);
                    }
                } else {

                }

            }
        } catch (SQLException e) {

        }

    }//GEN-LAST:event_deleteBtnActionPerformed

    private void searchFieldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchFieldKeyReleased
        // TODO add your handling code here:

        String searchKey = searchField.getText();
        if (searchKey.trim().length() == 0) {
            tableRowSorter.setRowFilter(null);
        } else {
            tableRowSorter.setRowFilter(RowFilter.regexFilter("(?i)" + searchKey));
        }
    }//GEN-LAST:event_searchFieldKeyReleased

