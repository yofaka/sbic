    private void saveItemCategoryBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveItemCategoryBtnActionPerformed

        try {

            if (newForm) {

                if (nameField.getText().equals("")) {

                    JOptionPane.showMessageDialog(this, "Enter Name", "Add Supplier", JOptionPane.ERROR_MESSAGE);
                } else if (!Validator.isProperName(nameField)) {

                    JOptionPane.showMessageDialog(this, "Name can't be longer than 30 characters.", "Add Supplier", JOptionPane.ERROR_MESSAGE);
                } else {

                    if (Supplier.nameExists(nameField.getText(), "")) {

                        JOptionPane.showMessageDialog(this, "Supplier Name Exists", "Add Supplier", JOptionPane.ERROR_MESSAGE);

                    } else if (Supplier.tinExists(tinField.getText(), "")) {

                        JOptionPane.showMessageDialog(this, "Supplier TIN Exists", "Add Supplier", JOptionPane.ERROR_MESSAGE);
                    } else {

                        Supplier newSupplier = new Supplier(nameField.getText(), descriptionField.getText(), tinField.getText(), telephoneField.getText());
                        if (newSupplier.save()) {
                            JOptionPane.showMessageDialog(this, "Supplier Registered Succesfully", "Add Supplier", JOptionPane.INFORMATION_MESSAGE);
                            supplierForm.setVisible(false);
                            loadSuppliers();
                        }
                    }

                }

            } else {

                if (nameField.getText().equals("")) {

                    JOptionPane.showMessageDialog(this, "Enter Name", "Edit Supplier", JOptionPane.ERROR_MESSAGE);
                } else if (!Validator.isProperName(nameField)) {

                    JOptionPane.showMessageDialog(this, "Name can't be longer than 30 characters.", "Add Supplier", JOptionPane.ERROR_MESSAGE);
                } else {

                    if (Supplier.nameExists(nameField.getText(), selectedSupplier.getName())) {

                        JOptionPane.showMessageDialog(this, "Supplier Name Exists", "Edit Supplier", JOptionPane.ERROR_MESSAGE);

                    } else {

                        selectedSupplier.setName(nameField.getText());
                        selectedSupplier.setDescription(descriptionField.getText());

                        if (selectedSupplier.save()) {
                            JOptionPane.showMessageDialog(this, "Supplier Updated Succesfully", "Edit Supplier", JOptionPane.INFORMATION_MESSAGE);
                            supplierForm.setVisible(false);
                            loadSuppliers();
                        }
                    }

                }
            }

        } catch (SQLException ex) {
            Logger.getLogger(Suppliers.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_saveItemCategoryBtnActionPerformed

