    void loadSuppliers() throws SQLException {

        searchField.setText("");

        if (!Session.getLoggedInUser().getRole().equals("Administrator")) {

            addBtn.setEnabled(false);
        }

        editBtn.setEnabled(false);

        deleteBtn.setEnabled(false);

        String tableColumns[] = {"No", "Serial", "Name", "Description", "TIN", "Telephone"};

        suppliers = Supplier.findAll();

        Object tableDataRows[][] = new Object[suppliers.length][6];

        int numberOfRows = 0;

        for (Supplier supplier : suppliers) {

            tableDataRows[numberOfRows][0] = (numberOfRows + 1);
            tableDataRows[numberOfRows][1] = supplier.getId();
            tableDataRows[numberOfRows][2] = supplier.getName();
            tableDataRows[numberOfRows][3] = supplier.getDescription();
            tableDataRows[numberOfRows][3] = supplier.getTIN();
            tableDataRows[numberOfRows][3] = supplier.getTelephone();

            numberOfRows++;
        }

        TableModel itemCategoriesTableModel = new DefaultTableModel(tableDataRows, tableColumns) {

            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        suppliersTable.setModel(itemCategoriesTableModel);

        ListSelectionModel itemCategoriesTableSelectionModel = suppliersTable.getSelectionModel();

        tableRowSorter = new TableRowSorter(suppliersTable.getModel());

        suppliersTable.setRowSorter(tableRowSorter);

        itemCategoriesTableSelectionModel.addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent e) {

                if (itemCategoriesTableSelectionModel.getMaxSelectionIndex() == -1) {

                    editBtn.setEnabled(false);

                    deleteBtn.setEnabled(false);

                } else {

                    if (Session.getLoggedInUser().getRole().equals("Administrator")) {
                        editBtn.setEnabled(true);
                        
                        deleteBtn.setEnabled(true);
                        
                    }
                    selectedSupplier = Supplier.find((int) suppliersTable.getValueAt(suppliersTable.getSelectedRow(), 1));
                }
            }
        });
    }

